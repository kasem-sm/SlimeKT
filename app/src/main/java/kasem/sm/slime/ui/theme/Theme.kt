@Composable
fun SlimeTheme(
    context: Context = LocalContext.current,
    dynamic: Boolean = isAtLeastS,
    dark: Boolean = context.isSystemInDarkTheme,
    content: @Composable () -> Unit,
) {
    val colors = when {
        dynamic && dark -> dynamicDarkColorScheme(context)
        dynamic && !dark -> dynamicLightColorScheme(context)
        dark -> DarkThemeColors
        else -> LightThemeColors
    }

    CompositionLocalProvider(LocalSlimeFont provides SlimeFontFamily()) {
        MaterialTheme(
            colorScheme = colors,
            content = content
        )
    }
}

val isAtLeastS get() = Build.VERSION.SDK_INT >= 31.0

private val Context.isSystemInDarkTheme: Boolean
    get() = resources.configuration.uiMode and Configuration.UI_MODE_NIGHT_MASK == Configuration.UI_MODE_NIGHT_YES
